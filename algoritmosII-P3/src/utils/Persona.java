package utils;

public class Persona {

	String cedula;
	int clave;

	public Persona() {

	}

	public String getCedula() {
		return cedula;
	}

	public void setCedula(String ci) {
		this.cedula = ci;
	}

	public int getClave() {
		return clave;
	}

	public void setClave(int c) {
		this.clave = c;
	}

}
